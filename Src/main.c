/* TaskUART function */
void TaskUART(void const * argument)
{

  /* USER CODE BEGIN 5 */

	//! Zmienne lokalne:
	char data[50]; // Tablica przechowujaca wysylana wiadomosc.
	uint16_t size = 0; // Rozmiar wysylanej wiadomosci

	char rcvChar = '0';	// odebrany z kolejki znak

	uint16_t dutyCycle;

	uint8_t i = 0;		// iterator pêtli

	uint8_t flag = 0;

	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);

	//! Inicjalizacje:
	//Wyslanie powitalnej wiadomosci:
	//size = sprintf(data, "Set PWM duty cycle in us (0 - 20 000).\n\r");
	//HAL_UART_Transmit_IT(&huart2, data, size); // Rozpoczecie nadawania danych z wykorzystaniem przerwan

	// uruchomienie UART w trybie nas³uchiwania na przerwaniach
	HAL_UART_Receive_IT(&huart2, (uint8_t*)&UART_Received, UART_RCV_NUM);

  /* Infinite loop */
  for(;;)
  {
	  // Odebranie znaku z nieskonczonym oczekiwaniem z przerwan UART:
	  xQueueReceive(QueueUARTHandle, (void*)&rcvChar, portMAX_DELAY);

	  if ( rcvChar == 'M' || rcvChar == 'N' )
	  {
		  //HAL_UART_Transmit_IT(&huart2, (uint8_t*)&rcvChar, 1);	// odeslanie znaku
		  size = sprintf(data, "%c", rcvChar);
		  HAL_UART_Transmit_IT(&huart2, (uint8_t*)data, size);	// odeslanie znaku
	  }

	  if ( rcvChar == 'F' ) // strza³
		  HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, 1);

	  if ( rcvChar == 'S' ) // zwolnienie strza³u
		  HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, 0);

	  if ( rcvChar == 'P' ) // ustalenie pozycji
		  flag = 1;
	  if ( rcvChar >= '0' && rcvChar <= '9' )
		  data[i++] = rcvChar;	// zapisuje cyfry dopoki nie napotka znaku konca linii
	  else if ( (rcvChar == ' ' || rcvChar == '\r') )
	  {
		  i = 0;	// zeruje licznik znakow
		  dutyCycle = (uint16_t)atoi(data) - 1000;	// konwersja pozwalajaca odczytanie liczby 16-bitowej
		  if (flag == 1)
		  {
			  dutyCycle = SERVO_RIGHT_POS + 1010 + (int16_t)((1010 - (int16_t)dutyCycle + SERVO_RIGHT_POS));
			  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, dutyCycle);
			  flag = 2;
		  }
		  else if (flag == 2)
		  {
			  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, dutyCycle);
			  flag = 0;
		  }
	  }
  }
  /* USER CODE END 5 */ 
}

